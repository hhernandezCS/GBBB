        axios.post('nomina/marcadas-justificaciones/obtener', data)
        .then(function (response) {
            if (response.data.status === 'success') {
                cb(response.data.result)
            } else {
                errorCb(response.data.result)
            }
        })
        .catch(function (error) {
            errorCb(error)
        })
